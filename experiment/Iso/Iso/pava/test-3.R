library(testthat)

.Random.seed <<- .ext.seed

test_that("pava", {
    z <- c(5, 5.05, 5.1, 5.15, 4, 4.05, 4.1, 4.15, 4.2, 3, 3.05, 3.1, 3.15, 3.2, 3.25, 2, 2.05, 2.1, 2.15, 2.2, 2.25, 2.3, 
        1, 1.05, 1.1, 1.15, 1.2, 1.25, 1.3, 1.35)
    expect_equal(Iso:::pava(y = z, decreasing = TRUE), c(5.075, 5.075, 5.075, 5.075, 4.1, 4.1, 4.1, 4.1, 4.1, 3.125, 3.125, 
        3.125, 3.125, 3.125, 3.125, 2.15, 2.15, 2.15, 2.15, 2.15, 2.15, 2.15, 1.175, 1.175, 1.175, 1.175, 1.175, 1.175, 1.175, 
        1.175))
})
