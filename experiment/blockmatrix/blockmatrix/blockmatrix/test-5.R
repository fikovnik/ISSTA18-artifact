library(testthat)

.Random.seed <<- .ext.seed

test_that("blockmatrix", {
    F <- structure(c(7.55953307142448, 11.3201133457302, 9.69336140592153, 8.21869156602, 9.82808264424038, 11.2146746991726, 
        11.895193461265, 9.5695308683938, 9.74273061723107), .Dim = c(3L, 3L))
    D <- structure(c(3.93728590094758, 5.30486965422349, 6.28664539270111, 2.61113929888766, 3.72121123318263, 3.86667866360634, 
        4.63595039807007, 3.71574707858393, 1.34354457909522), .Dim = c(3L, 3L))
    expect_equal(blockmatrix:::blockmatrix(dim = c(2, 2), names = c("0", "F", "D", "0"), F = F, D = D), structure(list(F = structure(c(7.55953307142448, 
        11.3201133457302, 9.69336140592153, 8.21869156602, 9.82808264424038, 11.2146746991726, 11.895193461265, 9.5695308683938, 
        9.74273061723107), .Dim = c(3L, 3L)), D = structure(c(3.93728590094758, 5.30486965422349, 6.28664539270111, 2.61113929888766, 
        3.72121123318263, 3.86667866360634, 4.63595039807007, 3.71574707858393, 1.34354457909522), .Dim = c(3L, 3L)), value = structure(c("0", 
        "F", "D", "0"), .Dim = c(2L, 2L))), class = "blockmatrix"))
})
