library(testthat)

.Random.seed <<- .ext.seed

test_that("make_parameter_mat", {
    ypar <- c(5, 6, 7, 8)
    xpar <- 1:4
    rpar <- 9
    ps_new <- structure(c(-0.735365272128125, -0.690761140849801, 0.940706239512013, 0.945563286338958, -0.0359094291951983, 
        0.630008888748633, -0.0547318113079465, 0.457405294053844, -0.398360521272436), .Dim = c(9L, 1L))
    expect_equal(mdsdt:::make_parameter_mat(xpar = xpar, ypar = ypar, rpar = rpar, ps_new = ps_new), structure(c(-0.735365272128125, 
        -0.690761140849801, 0.940706239512013, 0.945563286338958, 1, 1, 1, 1, -0.0359094291951983, 0.630008888748633, -0.0547318113079465, 
        0.457405294053844, 1, 1, 1, 1, -0.398360521272436, -0.398360521272436, -0.398360521272436, -0.398360521272436), .Dim = 4:5, 
        .Dimnames = list(NULL, c("mu", "sigma", "nu", "tau", "rho"))))
})
