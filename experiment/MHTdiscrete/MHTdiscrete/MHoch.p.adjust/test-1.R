library(testthat)

.Random.seed <<- .ext.seed

test_that("MHoch.p.adjust", {
    p.set <- list(c(0.00390625, 0.03515625, 0.14453125, 0.36328125, 0.63671875, 0.85546875, 0.96484375, 0.99609375, 1), c(0.0009765625, 
        0.015625, 0.103515625, 0.3671875, 0.7626953125, 1), c(0.004096, 0.04096, 0.1792, 0.45568, 0.76672, 0.953344, 1))
    p <- c(0.03515625, 0.015625, 0.04096)
    expect_equal(MHTdiscrete:::MHoch.p.adjust(p = p, p.set = p.set), c(0.03925225, 0.02362725, 0.04096))
})
