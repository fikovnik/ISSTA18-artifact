library(testthat)

.Random.seed <<- .ext.seed

test_that("print.audioSample", {
    x <- structure(c(0.0998334166468282, 0.198669330795061, 0.29552020666134, 0.389418342308651, 0.479425538604203, 0.564642473395035, 
        0.644217687237691, 0.717356090899523, 0.783326909627483, 0.841470984807897), rate = 8000, bits = 16L, class = "audioSample")
    expect_equal(audio:::print.audioSample(x = x), c(0.0998334166468282, 0.198669330795061, 0.29552020666134, 0.389418342308651, 
        0.479425538604203, 0.564642473395035, 0.644217687237691, 0.717356090899523, 0.783326909627483, 0.841470984807897))
})
